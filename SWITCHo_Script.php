<?php

$opcao = 0;  //Declarando variável
switch($opcao) {  //Criando Switch-case
	case 1:   //Caso seja 1 faça
	echo "Número 1";  //Imprime valor na tela
	break;  //Sai do Loop
	case 2:
	echo "Número 2";
	break;
	case 3:
	echo "Número 3";
	break;
	default:  //Caso não seja nenhuma das opções faça!!
	echo "Número diferente dos de cima!!!!";  //Imprime na tela

}





